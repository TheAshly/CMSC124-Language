fun main(){
    // Calls Scanner
    val scanner = Scanner()
    val parser = Parser()
    val printer = Printer()
    while(true) {
        print(">")
        val input = readln()
        println(input)
        val tokens = scanner.scanLine(input)
        val node = parser.parseTokens(tokens)
        printer.parserPrinter(node)
        println()
        System.gc()
    }
}